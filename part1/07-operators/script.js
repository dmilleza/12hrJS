// arithmetic operators
let x;

x = 5 + 5;
x = 5 - 1;
x = 5 * 5;
x = 5 / 5;

// % gets the remainder
x = 7 % 5;
// 2

// concatenation
x = 'hello' + 'world';
// hello world

// exponent
x = 2 ** 3;
// 8

// increment
x = 1;
// x = x + 1
x++;
// 2

// decrement
// x = x - 1
x--;

// assignment operators
x = 10;

x += 5;
// same as x = x + 5

// comparison operators
x = 2 == 2
// true 
x = 2 === 2
// true
x = 2 == "2"
// true
x = 2 === "2"
// false
// pretty much always use === 

x = 2 != 2
// false because 2 is equal to 2
x = 2 != "2"
// false
x = 2 !== "2"
// true
x = 10 > 5
x = 10 < 5
x = 10 <= 5
x = 10 >= 5
console.log(x);